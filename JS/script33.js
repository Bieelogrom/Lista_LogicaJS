var valor_um = parseInt(prompt("Digite o primeiro valor: "))
var valor_dois = parseInt(prompt("Digite o segundo valor: "))

if(valor_um == valor_dois){
    alert("Valores iguais!")
}else if(valor_um > valor_dois){
    alert("Primeiro valor é maior que o segundo.")
}else{
    alert("Segundo valor é maior que o primeiro.")
}
