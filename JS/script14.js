//14) Ler um valor e escrever a mensagem É MAIOR QUE 10! se o valor lido for maior que 10, caso 
// contrário escrever NÃO É MAIOR QUE 10! 

var valor = parseInt(prompt("Digite o valor desejado: "))

if(valor > 10){
    alert("O valor é maior que dez\n"+ valor)
}else{
    alert("O valor é menor que dez\n"+ valor)

}